# 파이썬의 주석 #
""" 파이썬의
여러줄 주석"""

# 숫자 자료형
## 1. 정수형
print(1, 3, 0) # cf. print()가 여러 arguments를 받을 수 있음
print(1, 3, 0, sep="") # sep: arguments 사이의 구분 기호 - 기본이 " "
print(1, 3, 0, end=" | ") # end: 다음 출력문과의 구분 기호 - 기본이 개행 문자
print(1, 3, 0)

## 2. 실수형
print(1.1, 3.14)

# 문자열 자료형 - "..." 혹은 '...' 
print("Hi")

# boolean형 자료형 - True 혹은 False
print(True)
print(False)
